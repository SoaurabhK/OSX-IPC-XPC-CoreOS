#include <Foundation/Foundation.h>
#import "TicketAgent.h"
#import <xpc/xpc.h>
#import "XPCKit.h"

static void peer_event_handler(xpc_connection_t peer, xpc_object_t  event) {
    xpc_connection_t remote = NULL;
    if (xpc_get_type(event) == XPC_TYPE_DICTIONARY) {
        //Decompose and handle message
        remote = xpc_dictionary_get_remote_connection(event);
        
        xpc_object_t reply = xpc_dictionary_create_reply(event);
        xpc_dictionary_set_int64(reply, "returnCode", 0);
        
        xpc_connection_send_message(remote, reply);
        xpc_release(reply);
        
    } else {
        if (event == XPC_ERROR_CONNECTION_INVALID) {
            //Error indicates that peer has closed the connection
            //Tear down any associated data structure
        } else {
            //Error indicates that service will be terminated soon.
            //Flush all buffers, finish all work etc.
        }
        xpc_release(peer);
    }
}

static void new_connection_handler(xpc_connection_t peer) {
    xpc_connection_set_event_handler(peer, ^(xpc_object_t event) {
        peer_event_handler(peer, event);
    });
    xpc_connection_resume(xpc_retain(peer));
}

int main(int argc, const char *argv[])
{
    xpc_main(new_connection_handler);
    
    return 0;
}
